package org.spongycastle.pqc.math.ntru.polynomial;

import org.spongycastle.util.Arrays;

public class SparseTernaryPolynomial implements TernaryPolynomial {
    public final boolean equals(Object obj) {
        if (this == obj) {
            return true;
        }
        if (obj == null || getClass() != obj.getClass()) {
            return false;
        }
        SparseTernaryPolynomial sparseTernaryPolynomial = (SparseTernaryPolynomial) obj;
        sparseTernaryPolynomial.getClass();
        sparseTernaryPolynomial.getClass();
        if (!Arrays.b((int[]) null, (int[]) null)) {
            return false;
        }
        sparseTernaryPolynomial.getClass();
        if (!Arrays.b((int[]) null, (int[]) null)) {
            return false;
        }
        return true;
    }

    public final int hashCode() {
        int u = Arrays.u((int[]) null);
        return Arrays.u((int[]) null) + ((u + ((0 + 31) * 31)) * 31);
    }
}
